package dataType;

public class FloatDemo {
	public static void main(String[] args) {
		
		float f1 = 45.120f;
		float f2 = -12.369f;
		float f3 = -960.4563f;
		
		System.out.println(f1);
		System.out.println("f1 = " +f1);
		System.out.println(f2);
		System.out.println("f2 = " +f2);
		System.out.println(f3);
		System.out.println("f3 = " +f3);
	}

}
