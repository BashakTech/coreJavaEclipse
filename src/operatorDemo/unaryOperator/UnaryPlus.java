package operatorDemo.unaryOperator;

public class UnaryPlus {
	public static void main(String[] args) {
		
		int x = 10, y = 6;
		int result;
		
		System.out.println("int x = " +x);
		System.out.println("int y = " +y);
		
		result = +x;		
		System.out.println("result  = " +result);
		
		result = -x;
		System.out.println("result = " +result);
		
	}

}
